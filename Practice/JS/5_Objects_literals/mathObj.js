// Properties
console.log(Math.PI);
console.log(Math.E);

// Methods
console.log(Math.abs(-5)); // Absolute value

console.log(Math.round(4.7)); // Rounds to the nearest integer
console.log(Math.round(4.3));

console.log(Math.ceil(4.3)); // Rounds up to the nearest integer
console.log(Math.ceil(4.7));

console.log(Math.floor(4.7)); // Rounds down to the nearest integer
console.log(Math.floor(4.3));

console.log(Math.sqrt(16)); // Square root
console.log(Math.pow(2, 3)); // Power

console.log(Math.max(1, 5, 3, 9)); // Maximum value
console.log(Math.min(1, 5, 3, 9)); // Minimum value

console.log(Math.random()); // Random number between 0 and 1