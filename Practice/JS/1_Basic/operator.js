// Arithmetic Operator
let a = 10;
let b = 5;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);

// Unary Operator
console.log(a++);
console.log(++b);

console.log(a--);
console.log(--b);

// Assignment Operators
let c = 10;
let d = 3;
console.log(c=d);
console.log(c += d);
console.log(d -= c);
console.log(a *= b);
console.log(a /= b);
console.log(a %= b);
console.log(a **= b);

//Comparison Operators
console.log(a == b);
console.log(a != b);
console.log(a >= b);
console.log(a > b);
console.log(a <= b);
console.log(a < b);
console.log(a === b);
console.log(a !== b);

//Logical Operators
console.log(a && b);
console.log(a || b);
console.log(! b);
